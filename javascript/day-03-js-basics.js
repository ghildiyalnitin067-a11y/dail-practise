// Day 3 - JavaScript Basics (Till Loops)

// Variables
let name = "Nitin";
let age = 20;

console.log("Name:", name);
console.log("Age:", age);

// If-else
if (age >= 18) {
  console.log("Adult");
} else {
  console.log("Minor");
}

// For loop
for (let i = 1; i <= 5; i++) {
  console.log("For loop count:", i);
}

// While loop
let j = 1;
while (j <= 3) {
  console.log("While loop count:", j);
  j++;
}
